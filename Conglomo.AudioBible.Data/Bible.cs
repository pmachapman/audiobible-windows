        /// <summary>
        /// Gets the name of the track.
        /// </summary>
        /// <param name="fileIndex">Index of the file.</param>
        /// <returns>The name of the track.</returns>
        public static string GetTrackName(int fileIndex)
        {
            // Set up variables
            string trackName = string.Empty;
            fileIndex++;

            // Get the book name
            for (int i = 0; i < books.Count; i++)
            {
                if (fileIndex > chapters[i])
                {
                    fileIndex -= chapters[i];
                }
                else
                {
                    trackName = books[i];

                    // See if this is a one chapter book
                    if (chapters[i] == 1)
                    {
                        return trackName;
                    }
                    else
                    {
                        break;
                    }
                }
            }

            // Get the chapter number
            if (!string.IsNullOrEmpty(trackName))
            {
                trackName += " " + fileIndex.ToString(CultureInfo.CurrentCulture);
            }

            return trackName;
        }

        /// <summary>
        /// Gets the file index.
        /// </summary>
        /// <param name="bookIndex">The book index.</param>
        /// <param name="chapterIndex">The chapter index.</param>
        /// <returns>The file index.</returns>
        public static int GetFileIndex(int bookIndex, int chapterIndex)
        {
            // Set up the index
            int index = chapterIndex;

            // Ensure no overflow
            if (bookIndex == int.MinValue)
            {
                bookIndex = 0;
            }

            // Add each of the preceeding chapters to get the correct index
            for (int i = bookIndex - 1; i >= 0; i--)
            {
                index += chapters[i];
            }

            return index;
        }

        /// <summary>
        /// Gets the next file index.
